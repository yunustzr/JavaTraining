public class Q6
{
	public static void main(String[] args) {
	    for (int i=0;i<10 ;++i ){
	        if (i==3){
	            System.out.println("Sayi degerimiz: "+i);
	            System.out.println("CONTİNUE ifadesiyle döngü devam ediyor.");
	            continue;
	            
	        } 
	        if (i==5){
	            System.out.println("Sayi degerimiz: "+i);
	            System.out.println("BREAK ifadesiyle döngüyü durdurma.");
	            break;
	        } 
	        /*
			* Devam etmesi için continue ifadesi kullanilir.
			* Cevap B
			*/
	    }
	}
}
